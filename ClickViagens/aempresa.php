		<h1> A empresa </h1>
		
		<h6>Descrição da empresa:</h6>

			<p>A Click Viagens é uma agência de viagens, turismo e eventos, localizada em
		Santa Bárbara, Minas Gerais, que oferece um leque diversificado de opções
		de produtos e serviços turísticos. Nossos pacotes e serviços envolvem viagens de lazer, de viagens
		corporativas, Viagens de incentivo e para eventos específicos, viagens
		nacionais e internacionais através de parcerias com diversas operadoras
		afim de atender o nosso cliente da melhor forma e de acordo com seu
		perfil. </p>
		
		<hr>

		<h6>Parcerias:</h6>

			<p>A Click Viagens possui parcerias com as melhores e mais conceituadas
		operadoras do Brasil. Entre em contato conosco e lhe passaremos a melhor
		opção para sua viagem. </p>
		
		<hr>

		<h6>Histórico:</h6>

			<p>Fundada em 19 de Março de 2013 pela mineira Cyntia Quintão Pinto
		Coelho, a Click Viagens tem o objetivo de conquistar a confiança e passar
		credibilidade perante nossos clientes, oferecendo soluções em viagens,
		turismo e eventos com segurança, qualidade e excelência, realizando sonhos
		e contribuindo para o desenvolvimento sóciocultural. Nosso objetivo não
		é só realizar o sonho de seus clientes, mas também superar suas expectativas
		diante das viagens que a Click fornecer. </p>
		
		<hr>

		<h6>Missão:</h6>	

			<p>Nossa missão é proporcionar a todos os nossos clientes o prazer de viajar e
		criar neles a emoção de visitar novos lugares. Formatar produtos e adaptar o preço
		desses à condição de pagamento dos clientes. </p>
		
		<hr>

		<h6>Visão:</h6>

			<p>Tornar o turismo acessível a todas as pessoas oferecendo produtos,
		serviços e atendimento de qualidade a preços justos, dedicando-se
		diariamente a realizar o sonho de cada cliente.	Tornar-se referência no
		segmento de agências de viagens, turismo e eventos, atuando com foco na qualidade,
		inovação e satisfação dos clientes oferecendo competência em todas as atividades. </p>
		
